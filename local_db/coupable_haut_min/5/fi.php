function gwc_coupable_haut_min_5_fi($code){switch($code){
case'fihoo':return array('fibre');
case'fioho':return array('fiche');
case'fidol':return array('fidel');
case'fiooo':return array('fiere','fiers');
case'filoo':return array('filer','files','filon');
case'filot':return array('filet');
case'fillo':return array('fille');
case'filmo':return array('filme','films');
case'fiool':return array('final');
case'fioio':return array('finie','finir','finis');
case'fioit':return array('finit');
case'fiomo':return array('firme');
case'fiiio':return array('fixe','fuir');
case'fiiit':return array('fuit');
case'fiito':return array('fute','futs');
default: return array();}}
